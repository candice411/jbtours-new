              <p>Die eerste inniBosveld   Fees&nbsp;was in Februarie 2010 op <STRONG><strong>Kurt   Darren</strong></STRONG>&nbsp;(bekende en gewilde sanger) se wildsplaas   buite Naboomspruit aangebied. Die organiseerders het gehoop &amp; vertrou dat   daar 5000 mense sou opdaag. Daar daag toe wel bykans&nbsp;18000 op! Dit het   talle&nbsp;nuwe eise aan die organiseerders gestel en 2011 se fees wat oor 2 dae (18   &amp; 19 Februarie) aangebied word, sal ietwat anders daar uitsien, PLUS al die   tekortkominge van die 2010 Fees sal aangespreek word. </p>
              <p>Ons maatskappy is weer   versoek om die bustoere soontoe te re&euml;l en het onlangs weer op Kurt se plaas   gaan kuier&nbsp;&amp; vergader om ons van die nuutste logistieke re&euml;lings te   vergewis. Ons het een bus vir 2010 beplan &amp; begroot en daar het toe ses (6)   busse gery! Vir baie mense was dit die <STRONG><strong>maklikste, veiligste,   gerieflikste en lekkerste</strong></STRONG> wyse om die Fees by te   woon. Ons re&euml;l vir 2011 <STRONG><strong>weer</strong></STRONG> die bustoere soontoe. </p>
              <p>JB Train Tours bied&nbsp;in   2011&nbsp;ook&nbsp;drie verskillende&nbsp;<STRONG><strong>naweek-buspakkette</strong></STRONG> na die Fees toe aan. Die busse&nbsp;vertrek op Vrydagoggend 18 Feb 2011 vanaf Jo'burg   lughawe (ORTIA)&nbsp;en reis oor Centurion en Pretoria na die&nbsp;onderskeie bestemmings in en   buite Naboomspruit. Passasiers kan&nbsp;langs die roete&nbsp;(Centurion, Menlopark en   Montana in Pretoria) ook opklim. </p>
              <p>Die <STRONG><strong>naweek-bustoerpakkette</strong></STRONG> sluit&nbsp;retoer-busvervoer, 2 nagte akkommodasie, 2 etes per dag,&nbsp;toegang tot beide   die Feesdae en&nbsp;'n toergids&nbsp;in.&nbsp;Met die een toerpakket word in die <STRONG><strong>skoolkoshuis</strong></STRONG> op Naboomspruit oornag. Die&nbsp;tweede toerpakket maak voorsiening vir verblyf in <STRONG><strong>luukse   tente</strong></STRONG> by <STRONG><strong>Die   Oog</strong></STRONG> minerale bron, terwyl die derde pakket verblyf in <STRONG><strong>chalets</strong></STRONG> op 'n <STRONG><strong>wildsplaas</strong></STRONG> verseker. Al drie hierdie toer-opsies gaan heerlike kuiergeleenthede en waarde   vir geld bied. Akkommodasie in die omgewing is MIN oor hierdie naweek. <U><a href="http://www.jbtours.co.za/2011-innibosveld-fees.php">Meer inligting</a></U>&nbsp;oor die 3   naweekpakkette. </p>
              <p>Die <STRONG><strong>dagtoer-buspakkette</strong></STRONG> sal ook weer aangebied word. Daar word op Vrydag- en Saterdagoggende vanuit JHB   &amp; PTA vertrek.&nbsp; Kostes beloop <STRONG><strong>R450 </strong></STRONG>pp (Vrydag) en <STRONG><strong>R500</strong></STRONG> pp (Saterdag). <a href="http://www.jbtours.co.za/2011-innibosveld-fees.php">Meer   inligting</a>&nbsp;oor die dagpakkette. </p>
              <p>'n Verdere nuwigheid is dat   daar in 2011 op beide Feesdae 'n <STRONG><strong>middag-voorprogram</strong></STRONG> aangebied word waartydens minder-bekende kunstenaars sal optree. Diegene wat   vroeg arriveer sal derhalwe ook&nbsp;ekstra kyk- en luistergenot ervaar. </p>
