              <div style="float:left; padding:5px;"><img src="http://www.jbtours.co.za/images/newsletter/nov-2010/shosholoza-meyl.jpg" width="302" height="123" alt="Shosholoza Meyl tourism class"></div>
              <p>The articles in the   October&nbsp;&amp; November 2010 Newsletters, about the <STRONG><strong>suspension</strong></STRONG> of long distance passenger trains in South Africa for more than 3 months (12 Aug   to end of Nov 2010)&nbsp;caused an interesting outcry from many readers. Some blamed   JB Train Tours for it whilst others blamed the government or Transnet (and even   Spoornet and Metrorail!) or SA Tourism, etc. Fact is, whoever is to be blamed   caused <STRONG><strong>a lot of   injury</strong></STRONG> to many role players in the industry. However,   the biggest "injury" was caused to passengers who wanted to travel by rail.   Whether by going home, to work or just on holiday. </p>
              <p>On Wednesday 17th November   2010, JB Train Tours attended a workshop hosted by Shosholoza Meyl to be   informed about the current train scenario, the future of long distance trains in   South Africa and <STRONG><strong>Rail   Tourism</strong></STRONG> per se. According to acting CEO of Shosholoza   Meyl, Mr Paul M. Zikhali, ALL servives will be <STRONG><strong>'back to   normal'</strong></STRONG> as from 1st December 2010. Though, certain   routes will be served by combined trains. I.e.: Tourist Class and Economy Class   will share the same train. </p>
              <p>This announcement will have <STRONG><strong>no</strong></STRONG> immediate affect on our December 2010 tours from Jo'burg to Cape Town, because   all the originally scheduled train tours have been changed to luxury bus tours   and accommodation&nbsp;&amp; services en route have been booked and   paid. </p>
              <p>We (JB Train Tours) will   update our 2011 Year Programme accordingly - to be available   soon. </p>
              <p><STRONG><strong>Last minute   note:</strong></STRONG> The trains did NOT start running on the 1st   December 2010! </p>
