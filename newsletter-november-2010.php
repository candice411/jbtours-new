                                 <div style="float:left; padding:5px;"><img src="http://www.jbtours.co.za/images/newsletter/nov-2010/shosholoza-meyl.jpg" width="302" height="123" alt="Shosholoza Meyl tourism class"></div>
                                   <p>The article in the October 2010   Newsletter lead to several enquiries from concerned&nbsp;travellers about the future   of train tours in South Africa. We&nbsp;at JB Train Tours are still very enthusiastic   about Rail Tourism, but whether Shosholoza Meyl shares this view is doubtful.   Not only Rail Tourism, but the running of long distance trains, is in   jeopardy.                                   </p>
                                   <p>The contradictive statements   made and the lack of information supplied by Shosholoza Meyl, make it virtually   impossible to plan for the future. A recent press release stated that "trains   are running normal again', but if one goes to any station there is no Tourist Class train   ticket to be bought nor any train! Recently a senior manager at   Shosholoza Meyl informed us that <STRONG>NO Tourist Class</STRONG> train will   run for the rest of 2010 - including during the peak period. After we've changed   all eleven of our December 2010 train tours from Jo'burg to Cape Town to luxury   coach tours, affecting about 1300 people, the same manager denied that he ever   said it!! (No, they did not put anything in writing).                                   </p>
                                   <p>Currently our company is   playing a 'wait and see' game. JB Train Tours is Shosholoza Meyl's biggest   client, booking annually the most number of passengers on their trains, but we   are not treated accordingly. All this is affecting YOU, the client, because we   can't communicate correct up-to-date information to   you.                                   </p>
                                   <p>However, we are STILL committed   to Rail Tourism and we trust that the authorities will get their act together   and serve the country as a whole - very soon. The good news is that the <span style="color:#990099;"><STRONG>Premier Classe</STRONG></span> train between Jo'burg   and Cape Town is still running regularly.                                 </p>
