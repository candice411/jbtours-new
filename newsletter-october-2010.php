                    AFRICA?</STRONG></FONT></TD></TR></TBODY></TABLE>
              <P>For many years in the past it was common practice for people in 
              South Africa to go on holiday by train. It was their only means of 
              transport. Today there are still many people travelling to 
              far-away destinations by train on a regular basis. It is still 
              their only means of safe transport. Not only for holiday purposes, 
              but to get home or go to work.</P>
              <P>However, since 12th August 2010 Shosholoza Meyl (it is close on 
              8 weeks now!) have suspended all long distance passenger trains. 
              Initially it was for 2 weeks and thereafter on short notice. 
              Sometimes on a 48 hours basis and even shorter!</P>
              <P>The above mentioned lack of services have discomforted<EM> 
              <STRONG>MANY</STRONG></EM> people in South Africa. Not only people 
              travelling on JB Train Tours' tours, but people from all walks of 
              life. Government (Shosholoza Meyl) stands responsible for this 
              lack of service.</P>
              <P>Although no formal announcement has been made, the indications 
              are there that some train routes would be "back to normal" from 
              October 2010. Some routes would not be serviced again in future. 
              It seems that all the Tourist Class &amp; <SPAN 
              style="COLOR: #990099"><STRONG><EM>Premier 
              Classe</EM></STRONG></SPAN> services on the Jo'burg to Durban 
              route have been cancelled. Likewise for the CPT to PE and CPT to 
              Durban routes.</P>
              <P>Which trains remain? The <SPAN 
              style="COLOR: #990099"><STRONG><EM>Premier 
              Classe</EM></STRONG></SPAN> train between CPT &amp; Jo'burg and 
              the Trans Karoo Tourist Class train between CPT &amp; Jo'burg, as 
              well as the Algoa Tourist Class train between Jo'burg &amp; Port 
              Elizabeth.</P>
              <P>These changes do have a major effect on our scheduled train 
              tours for 2011 and beyond. We need now to re-invent, re-plan, 
              re-schedule and re-organise. WE'LL DO IT! Because we still want to 
              take people on train tours through our beautiful country. </P>
              <P>We will soon avail our updated 2011 Year programme to you. The 
              majority of the tours and dates will however remain unchanged.</P>
