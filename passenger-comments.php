		  <br />
		  Of course we were very disappointed, as we had enjoyed a previous train holiday and were looking forward to this.  However, we understand that we can't always be sure to get what we want, and so we &quot;go with the flow&quot;.<br />
		  <br />
		  We want to congratulte your company and staff for the quick changes they made, and for their good organisation at such short notice.  Although the hotel we stayed in on the first night (The Fountains) was not up to scratch, we did have the excitement of a false fire alarm to add to our adventure, and the Towers in Strand Street on our way home was superb - a real treat.<br />
		  <br />
		  Our chief joy was the way the places we visited and the plants we saw were made to come alive by our pleasant, organised, well-informed and patient guide, Kobus van der Merwe.  We cannot speak highly enough of him and the way he managed his charges throughout the trip - from Durban and back.  He really made our holiday worthwhile and helped us recover from our disappointment about the train, to say nothing of introducing us to a whole new attitude towards succulents - we now have a lovely little collection of our own which will always be a reminder of those magical few days. Thank you.<br />
		  <br />
		  Please pass our thanks and congratulations on to Kobus.<br />
		  <br />
		  If we may make one suggestion : many of the passengers found the 3 middle days to be very long and thus tiring - leaving Vredendal at 07h45 and returning at 18h45.  Perhaps an hour less, arriving back at the hotel in time to relax before supper might be easier especially for the older folk???<br />
		  <br />
		  Yours sincerely<br />
		  Geoff and Jean Thomassen</p>

