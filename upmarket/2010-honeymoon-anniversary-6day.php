			  <h2>DISCOVER CAPE TOWN </h2>
              <p class="style3"><strong>This 6-day tour package is a very romantic way of starting your marriage!</strong></p>
              <p>Johannesburg is one of the world's most diverse,  fast-paced and vibrant cities; in many ways it lives up to its reputation as the  New York of Africa, despite its vast, sprawling developments.</p>
              <p>Joburg (or Jozi, or Egoli) is the largest city in South Africa, and is the capital of Gauteng, the country's  wealthiest province. The city is one of largest metropolitan areas in the  world, and many consider it to be one Africa's most global cities, alongside Cairo and Cape    Town.<strong> </strong></p>
              <p>Before the FIFA 2010 Soccer World Cup, many people would have  frowned when you told them you're coming to Johannesburg on holiday. You would have to be  confronted with concerns about crime and traffic, and the very common  question&nbsp;<em>Jo'burg? What's there to actually do in Jo'burg?</em></p>
              <p><em>D</em>espite  it's undeniable issues, this fascinating city, which originally served as a  starting point for travels to the rest of South Africa and Africa, has fast  become one of the region's most popular, and well-run, tourism and business  centres.</p>
              <p>There are dozens of world-class museums, galleries, theatres, golf  courses, sporting stadiums, shopping centres, safaris, memorials and  attractions dotted throughout the city.</p>
              <p>On top of this all, Johannesburg  is considered to be the world's largest man-made forest (just take a look at it  as you fly in), and its cool winters, warm summers (with spectacular thunder  showers and lightning storms), and plentiful sunshine makes it an ideal place  to visit all year round.</p>
              <p>There are many ways of travelling from Cape Town to Johannesburg,  but travelling with JB Train Tours on the upmarket <strong>Premier Classe</strong><strong> </strong>train will give you the opportunity to see  so much more of South Africa.  You will travel through 4 of the 9 provinces of South   Africa (Western Cape, Northern Cape, North West  &amp; Gauteng); you&rsquo;ll experience the vastness  of the Karoo and the beautiful sunsets; you&rsquo;ll see Springbuck and some other  antelope en route and you&rsquo;ll enjoy the beauty of the vineyards of the Western Cape.</p>
              <p>Various popular tourist destinations are within close  range of the hotel. I.e.: Montecasino, The Cradle of Humankind, Sterkfontein Caves,  The  Rhino and Lion Nature Reserve, Soweto Day Trips.</p>
