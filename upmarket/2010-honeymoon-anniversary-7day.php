			  <h2>DISCOVER CAPE TOWN </h2>
              <p class="style3"><strong>This 7-day tour package is a very romantic &amp;  AFFORDABLE way of starting your marriage!</strong></p>
              <p>Go and spend your honeymoon in one of the MUST SEE places in South  Africa is Cape Town &ndash; the Mother City of South Africa. (Cape Town was listed Number 2 of &ldquo;Places to  visit before you die&rdquo; in an International survey). </p>
              <p>This is where Robben Island (where Nelson Mandela spent 27 years of his  life) is situated, fabulous Table Mountain, spectacular Cape Point, the V&amp;A  Waterfront, the wine routes of the Cape (including Great Constantia), Hout Bay and  Kirstenbosch Botanical Gardens (with its variety of  flowers). </p>
              <p>Historical Stellenbosch with its many Cape  Dutch houses and old university, the  town of Hermanus where whales could be seen in  season and various upmarket Golf Courses and Estates, are all located closely to  Cape Town.</p>
              <p>The West Coast with its supreme sea food and beautiful small fishermen&rsquo;s  villages, abundance of &lsquo;Fynbos&rsquo; vegetation and flowers are all within an hour&rsquo;s  drive from the capital.</p>
              <p>There are many ways of travelling from Johannesburg to Cape Town, but  travelling with JB Train Tours on the upmarket <strong>Premier Classe</strong> train (Photo  Gallery: <a href="http://www.jbtours.co.za/upmarket/photo-gallery.php">http://www.jbtours.co.za/upmarket/photo-gallery.php</a>) (only 2 people per compartment) will give you the  opportunity to see so much more of South Africa.  You will travel through 4 of the 9 provinces of South   Africa (Gauteng, North West, Northern Cape &amp;   Western Cape); you&rsquo;ll  experience the vastness of the Karoo and the beautiful sunsets; you&rsquo;ll see  Springbuck and some other antelope en route and you&rsquo;ll enjoy the beauty of the  vineyards of the Western Cape.<br />
                <strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong><br />
                Various popular tourist destinations are within close range of the  hotel. I.e.: &ldquo;Capart Theatre&rdquo; &amp; Long Street Restaurants (romantic dinners!)  and night clubs are within walking distance, whilst the V &amp; A Waterfront is  a brisk shuttle by bus or taxi away.</p>
              <p>This <strong>7</strong>-day tour is long  enough to experience a lot of new things and short enough to take you out of  office for only a couple of days.</p>
