			  <p><strong>JB Train Tours</strong> invite you to join one of the annual <strong>train</strong> tours to the <strong>2011 Pick n Pay Argus Cycle Race</strong> in Cape Town.  Are you tired of driving, looking for accommodation  and worrying about the logistics? We&rsquo;ll take you there &ndash; just like the past  number of years.</p>
              <p>When travelling by  train, you don&rsquo;t have to worry about increasing  petrol prices, 
                tollgate fees,  heavy traffic, driving back or sitting with stiff legs in your car. The long 
                road through the Karoo becomes shorter AND you can enjoy a cold beer en  route. It is 
                so much easier!              </p>
              <p>This tour is on  the Upmarket <strong>Premier  Classe</strong> train <div style="float:right;"><img src="images/packages/capetown-weekend/argus.jpg" width="94" height="177" alt="Argus" /></div><br />
                (<a href="http://www.jbtours.co.za/upmarket/photo-gallery.php">www.jbtours.co.za/upmarket/photo-gallery.php</a>) </p>
              <p>We also have 2 <strong>Budget </strong>Train Tours on the <strong>Tourist Class train</strong> that will be going to  the <strong>2011 Argus.</strong></p>
              <p>Accommodation over the &lsquo;Argus weekend&rsquo; in Cape Town is very scarce and expensive. We  were fortunate to secure a number of rooms in <strong>a 4-star Hotel</strong>! We stayed  in this hotel during the 2007, 2008, 2009 &amp; 2010 races and it has proven to  be very popular. (It is very close to the starting line of the race). This  package is <strong><u>real</u></strong> <strong>value for money.</strong></p>
              <p>This tour is not  only about The Argus, but about travelling &amp; staying well and enjoying Cape Town and its  amenities. The grapes are sweet, the weather normally good, the sea beautiful  and the mountain clear. After all, you have to take a number of days leave &ndash; so  enjoy it!</p>
              <p>The advantage of  this train is that you arrive on the Friday in Cape Town  and you&rsquo;ll have ample time to register for the race, visit the Expo and &lsquo;tune  your bike&rsquo; whilst enjoying a &lsquo;last minute&rsquo; ride in the Mother City.</p>
              <p><strong>Race information:</strong> Please read your <strong>Argus Cycle Race</strong> <strong>information brochure</strong> well or visit their website at <a href="http://www.cycletour.co.za">www.cycletour.co.za</a>              </p>
