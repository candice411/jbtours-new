              <p>One of the MUST SEE places in South Africa  is Cape Town &ndash; the Mother  City of South Africa. (Cape Town was listed Number 2 of &ldquo;Places to  visit before you die&rdquo; in an International survey). </p>
              <p>This is where the South African parliament is situated, as well as  Robben Island (where Nelson Mandela spent 27 years of his life), fabulous Table  Mountain, spectacular Cape Point, the V&amp;A Waterfront, the wine routes of  the Cape (including Great Constantia), Hout Bay and Kirstenbosch Botanical  Gardens (with its variety of flowers). </p>
              <p>Historical Stellenbosch with its many Cape  Dutch houses and old university, the  town of Hermanus where whales could be seen in  season and various upmarket Golf Courses and Estates, are all located closely to  Cape Town.</p>
              <p>The West Coast with its supreme sea food and beautiful  small fishermen&rsquo;s villages, abundance of &lsquo;Fynbos&rsquo; vegetation and flowers are  all within an hour&rsquo;s drive from the capital.</p>
              <p>There are many ways of travelling from Johannesburg  to Cape Town, but travelling with us on the upmarket <strong>Premier  Classe train</strong> will give you the opportunity to see so much more of South Africa. You will travel through four of the nine provinces  of South Africa (Gauteng, North West, Northern Cape &amp; Western Cape);  you&rsquo;ll experience the vastness of the Karoo and the beautiful sunsets; you&rsquo;ll  see Springbuck and some other antelope en route and you&rsquo;ll enjoy the beauty of  the vineyards of the Western Cape.</p>
              <p>Various popular tourist destinations are within close  range of the hotel. I.e.: &ldquo;Capart Theatre&rdquo; &amp; Long Street Restaurants are  within walking distance, whilst the V &amp; A Waterfront is a brisk shuttle by  bus or taxi away.              </p>
