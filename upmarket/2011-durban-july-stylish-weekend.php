			  <p>Unrivalled as Africa's greatest sporting and social event, 2 July 2011  will again attract more than 50 000 of the country's bold, brash and beautiful  to Greyville Racecourse in Durban for the R3 million Vodacom Durban July.<br />
                <br />
As they have for more than 100 years, the cream of  the continent's thoroughbreds will come together, some for the first time, for  the 2 200m battle from which one will emerge to join the illustrious band that  have triumphed in Africa's greatest race.</p>
			  <p align="center"><img src="images/packages/durban-july/durban-july-1.jpg" width="671" height="177" alt="Durban July" /></p>
			  <p>Generating  a massive nationwide betting spree of over R190-million on the day with  R36-million splurged on the outcome of the main race alone, millions around the  country will tune in by radio, television or even telephone to follow the  fortunes of their fancies as they are cheered from start to finish by the  excited racegoers on course.<br />
			    <br />
			    With 12 races on the day and the best horses in  the country competing in the various events, it provides a banquet of  exhilarating equine competition unrivalled in Africa. The kaleidoscope of colourful  and eye-stopping fashion, beautiful women and thousands of revellers enjoying  themselves in the marquees and picnic sites is enhanced by the growing sense of  anticipation to create an electrifying atmosphere as they line the course and  the gladiators step out on parade. </p>
