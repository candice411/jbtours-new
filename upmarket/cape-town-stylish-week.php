			  <h4>DISCOVER CAPE TOWN in 8 DAYS</h4>
			  <p>One of the MUST SEE places in South Africa  is Cape Town &ndash; the Mother  City of South Africa. (Cape Town was listed Number 2 of &ldquo;Places to  visit before you die&rdquo; in an International survey). </p>
			  <p>This is where the South African parliament is situated, as well as  Robben Island (where Nelson Mandela spent 27 years of his life), fabulous Table  Mountain, spectacular Cape Point, the V&amp;A Waterfront, the wine routes of  the Cape (including Great Constantia), Hout Bay and Kirstenbosch Botanical  Gardens (with its variety of flowers). </p>
			  <p>Historical Stellenbosch with its many Cape  Dutch houses and old university, the  town of Hermanus where whales could be seen in  season and various upmarket Golf Courses and Estates, are all located closely to  Cape Town.</p>
			  <p>The West Coast with its supreme sea food and beautiful small fishermen&rsquo;s  villages, abundance of &lsquo;Fynbos&rsquo; vegetation and flowers are all within hour&rsquo;s  drive from the capital.</p>
			  <p>There are many ways of travelling from Pretoria  and Johannesburg to Cape   Town, but travelling with us on the luxurious Premier Classe train  will give you the opportunity to see so much more of South Africa. You will travel through 4 of the nine provinces of  South Africa (Gauteng, North West, Northern Cape &amp; Western Cape);  you&rsquo;ll experience the vastness of the Karoo and the beautiful sunsets; you&rsquo;ll  see Springbuck and some other antelope en route and you&rsquo;ll enjoy the beauty of  the vineyards of the Western Cape.</p>
			  <p>This 8-day trip is very suitable for all ages, diplomats, business  people, families and visitors who only have a few days available to travel  around in Cape Town. <br />
                  <strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong><br />
			    Various popular tourist destinations are within close range of the  hotel. I.e.: &ldquo;Capart Theatre&rdquo; &amp; Long Street Restaurants are within walking  distance, whilst the V &amp; A Waterfront is a brisk shuttle by bus or taxi away.</p>
			  This 8-day tour is long enough to experience a lot of  new things and enjoy the Western Cape.
