				  <p><strong>Reporting to the station before departure:</strong></p>
				  <p>Passengers  should report on the day of departure about an hour before the train departure  time at the Premier Classe Lounge at the relevant station. Identify yourself at  the welcome desk and avail your Confirmation Letter (received from JB Train  Tours) if requested to do so.</p>
				  <p>Enjoy some  tea/coffee and biscuits. The train manager will inform you when to board the  train.</p>
				  <p><strong>Directions to stations:</strong></p>
				  <p>We will send you  the train station address &amp; map with your confirmation letter. </p>
				  <p><strong>Luggage:&nbsp; </strong><br />
				    Two medium-sized  suitcases or carry bags needed during the journey are classified as luggage of  a personal nature. Luggage is restricted to 20kg per full paying passenger.</p>
				  <p>Your luggage  will be labelled on arrival and taken to your compartment/coupe by the Premier  Classe staff.</p>
				  <p><strong>Dress Code:</strong><br />
				    Casual and  semi-formal clothing are allowed on the train. No need to dress up for dinner.</p>
				  <p><strong>Pets:</strong><br />
				    Pets are not  allowed on board except for trained guide dogs.</p>
				  <p><strong>Security:</strong><br />
				    Security  personnel on the train are there to ensure the safety of every passenger </p>
				  <p><strong>Smoking on train:</strong><br />
				    Smoking is prohibited  on the train (except for inside the smokers lounge) </p>
				  <p><strong>Medical Assistance:</strong><br />
				    Should a  passenger become ill on board, the Train Manager will arrange for assistance at  the nearest station. </p>
