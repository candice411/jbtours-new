			  <p align="center" class="heading1"><strong>Premier Classe Train</strong></p>
				  <p>Thanks for  enquiring about the upmarket Premier Classe train. This train is filling a gap  in the travel industry in South    Africa and it will encourage people to  travel again by train over long distances.</p>
				  <p><strong>Shosholoza Meyl</strong> launched its luxurious <strong>Premier Classe</strong> train in  2001 and upgraded it in 2006 and 2007. Since then it has grown steadily in  status and JB Train Tours is making use of it regularly. It is just about as  luxurious as the Blue Train, but at an affordable price to you.				    </p>
				  <p>It is a casual trip, but stylish. The number of  spaces on this luxury train is limited. Hence, we suggest you book well in  advance.</p>
				  <p>When travelling on  the <strong>Premier Classe</strong> train you are treated and pampered. All the fine  cuisine meals are <strong>included</strong> in the tour  price, as well as high tea, welcome drinks &amp; snacks and bedding. All drinks  and expenses of a personal nature  are for your own account. This is travelling by train like in the &lsquo;olden days&rsquo;!				    </p>
				  <p align="center"><img src="images/pic01.jpg" alt="Premier Classe train outside" width="147" height="137" /></p>
				  <p>The Premier  Classe train currently runs on <strong>four routes</strong> through South Africa:</p>
				  <p>1.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <strong>Johannesburg</strong> to <strong>Cape Town</strong> and <strong>Cape Town</strong> to <strong>Johannesburg</strong><br />
				    2.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <strong>Johannesburg</strong> to <strong>Durban</strong> and <strong>Durban</strong> to <strong>Johannesburg</strong><br />
				    3.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <strong>Cape Town</strong> to <strong>Port Elizabeth</strong> and <strong>Port Elizabeth</strong> to <strong>Cape Town</strong> (both legs via Oudtshoorn &amp;  George)<br />
				    4. &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <strong>Johannesburg</strong> to<strong> Hoedspruit </strong>and <strong>Hoedspruit</strong> to <strong>Johannesburg</strong></p>
