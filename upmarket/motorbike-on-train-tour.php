			  <p>JB Train Tours invites you to join  us on the <strong>Motorbike-on-Train Tour </strong>hosted from Johannesburg  to Cape Town  and return during 2010, travelling on the luxurious <strong>Premier</strong> <strong>Classe  Train</strong>. This tour is ideal for those bikers who prefer a bit more luxury, as well as for those who would like to  explore new destinations in a well structured, organised and safe manner. This is  the time and opportunity to do it and to enjoy the Western Cape and Boland at its best. <strong>24  Mountain passes; +-1020 km; in 4 days; tour guide; fun &amp; laughter.</strong> If  you are a &ldquo;big biker&rdquo; and you&rsquo;ve done all the roads and routes in Gauteng, Mpumalanga, KZN,  Free State  and the Lowveld, and you would like to ride <strong>curving mountain passes,</strong> <strong>1000&rsquo;s  of sweeping corners </strong>and <strong>coastal road routes </strong>&ndash; all on tarred roads -  please read on.</p>
			  <p align="center"><img src="images/packages/motorbike/motorbike-1.jpg" alt="Motorbikes" width="640" height="264" /></p>
			  <p>This tour does follow the same routes as our other Motorbike-on-Train tour,  hosted on the Trans Karoo Tourist Train and bikers on the Premier Classe train  tour will ride with the other tours&rsquo; bikers from Day 3 onwards. The rides  &lsquo;lost&rsquo; on Day 2 will be done on Day 6. Many bikers have asked for such a means  of travel and after some more thorough investigation and proper planning, we  are ready to take you from Gauteng to the Western Cape for a most  enjoyable <strong>motorbike-on-train tour </strong>on the <strong>Premier Classe </strong>train. Visit  places and travel roads that you&rsquo;ve never seen before (or heard off);  experience the scenic mountain passes with curves and corners galore; traverse  the coastal roads; dine at quaint restaurants and bush pubs en route;  accelerate in remote stretches and enjoy the &lsquo;fynbos&rsquo; and outdoor life in the  Western Cape &amp; Boland.</p>
			  <p>Yes, the info above is correct.  We&rsquo;ll traverse <strong>24 </strong>mountain passes (not repeating one pass at any stage  in the same direction) and travel more than a 1000km over the 4 days. There is ample  time available to relax and have fun at night. JB Train Tours<strong> </strong>invites you to join us on a holiday <strong>motorbike-on-train </strong>tour in <strong>2010 </strong>to Cape Town &amp; the Western Cape. This is a <strong>safe </strong>and <strong>secure tour </strong>for individuals, families and/or groups. (Please  note this is a <strong>TOUR, NOT </strong>a booze cruise, race or rally, neither a dull  affair). It is a tour with stoppages en route to be able to enjoy the beauty  &amp; natural scenery the Cape can offer.</p>
			  <p>We have scheduled all five tours on  the <strong>Premier Classe </strong>train with the Trans Karoo Tourist train 
			    tours during the year and you&rsquo;ll  notice that it is NOT during the  rainy season! This tour is a combination  &nbsp;of: travelling by train ( your bike will be  transported on the Trans Karoo Tourist train); enjoying Cape 
			    Town and the Western Cape; sleeping in a top class hotel<strong> </strong>with a swimming  pool and other facilities; </p>
			  <p>enjoying the beach and sea, visiting  the most popular tourist destinations and just relax. (All this at a very affordable<strong> </strong>rate at  upmarket facilities). We have a few pleasant surprises included in the  itinerary as well. The main purpose of this tour is to give bikers the  opportunity to ride their bikes in another  and interesting environment<strong> </strong>&ndash;  with curves almost unlimited. It is not a race, though there will be a number  of opportunities to use your horsepower to the full at certain spots. The different  daily routes will take you past some breathtaking scenery. Our cruising speeds  are within the legal limits.</p>
			  <p>The tours are scheduled to take place  in summer time &ndash; hence rain should not be a problem (as the Western Cape is a winter rainfall area), but  rather bring your foul weather gear with!</p>
			  <p>All the routes are on good <strong>tarred </strong>roads. A back-up vehicle will be on standby in case of emergency or breakdowns. <strong>If utilised, the expenses are for the biker&rsquo;s pocket. Please</strong> <strong>make  sure your bike is 100% roadworthy and in a good condition.</strong> The group is  limited to a <strong>maximum </strong>of <strong>22 motorbikes </strong>per tour &ndash; <strong>pillions </strong>are  obviously very welcome). I.e.: Max. 44 passengers per  tour. It is not compulsory for the driver or pillion to ride every day. It  could be exchanged for shopping and alternative sightseeing! This whole tour is a holiday. Are you tired of  phoning, looking for accommodation and worrying about the logistics? We&rsquo;ll take you there. Go and <strong>live </strong>this unique tour!</p>
