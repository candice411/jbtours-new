                  <td width="543" valign="top">We depart by bus from the hotel. Our journey continues  via the Paarl on the N1 which is the main route back to Gauteng! A visit is  paid to the Worcester Karoo Park, then  on to Slanghoek Valley and historical Tulbagh (&lsquo;Paddagang&rsquo;), picturesque  Riebeeck West and finish the day at Langebaan/Saldanha.  Dinner will be served. Overnight at the hotel.</td>
