			  <p>JB Train Tours invite you to  join us on our inaugural upmarket  Premier Classe train tour to the beautiful Namaqualand on the South African west coast. It is at  the end of the rainy season and the  flowers will be in its splendid beauty. There is nothing else like this natural  phenomenon.</p>
              <p>The flowers are normally in  abundance, the variety and colours almost unlimited, the &lsquo;veld&rsquo; lush and the  weather good. The 2011 flower season  promises to be as good.</p>
              <p>We are going to take you to  places where tour groups normally don&rsquo;t go. Just think about places like  Paternoster and Bird Island.</p>
              <p>You will do some wine tasting  and take pictures of a magnificent waterfall. Walk in the &lsquo;veld&rsquo; with flowers  from horizon to horizon. Bend your knees and discover small and tiny flowers.  Enjoy the grace of God.</p>
              <p>We&rsquo;ll travel on the luxurious <span class="style3"><strong>Premier Classe train</strong></span><strong></strong>, sleep in reputable hotels and travel by a  charter bus before joining the train again.</p>
              <p>Are you tired of driving,  looking for accommodation and worrying about the logistics? We&rsquo;ll take you  there. When travelling by train, you don&rsquo;t have to worry about increasing  petrol prices, tollgate fees, heavy traffic, driving back or sitting with stiff  legs in your car. It is so much easier, very safe and VERY price competitive. </p>
              <p>About  our<strong> Budget</strong> train tours (on the tourist class train): In 2007 all five  our train tours and in 2008 all 14, in 2009 all 19, and in 2010 all 19 were  fully booked. This year we&rsquo;ll be hosting 10 tours from Johannesburg and 5 from  Durban. This year will be our second year when we organise a Namaqualand Flower  Train tour on the <strong class="style3">Premier  Classe train</strong> from JHB to the Flowers &ndash; organised on request and  by huge demand. Book NOW!  Only three Namaqualand Flower train tours on the Premier Classe train in 2011!</p>
              <p>For the past four years the best  flowers were seen late July &amp; early August. Many people think that the  &lsquo;Namaqualand Flower&rsquo; is a Spring flower!<br />
                Baie mense s&ecirc; al baie jare  dat hulle graag na die blomme in Namakwaland wil gaan kyk. DOEN dit nou met die  luukse Premier Classe trein! Dit bly &lsquo;n heerlike voorreg om deur hierdie  geweste van die land te ry. Die mense is spesiaal (sout van die aarde), die  natuur is pragtig terwyl die kontreikos besonders is.</p>
              <p>Vanjaar wyk ons so &lsquo;n  bietjie van die beproefde resep af en besoek &lsquo;n klompie minder bekende, en dalk  mooier, bestemmings. Moenie dit mis nie. Bring vriende en familie saam. Vertel  die kuierklub om saam te reis. Bespreek NOU!</p>
