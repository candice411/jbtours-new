                  <td width="133" valign="top"><strong>Day 4: Wednesday</strong></td>
                  <td width="543" valign="top">After breakfast we  depart for Tietiesbaai, Cape Columbine &amp; Saldanha where the panoramic view  from &ldquo;Hoedjieskop&rdquo; will be enjoyed. This harbour town is well known for its  Military Academy, Steel Plant and terminal for the iron ore railway line from  Sishen in the Northern Cape. Enjoy tea at the famous(?) Paternoster Hotel. Some  of the best flowers are seen in this area. Here we'll also have a spectacular  view of the small bay with its fishing boats dotting the beach. 
                    <p>We travel to Velddrift, Port Owen with its beautiful water channels  (which reminds one of Venice), to  Laaiplek where we turn towards the picturesque town of Dwarskersbos. We follow  the route to Elands Bay, past the Rocherpan and one  of South Africa&rsquo;s few freshwater lakes, the Verlorenvlei. Both are world  renowned for the myriad of bird species to be found here. Normally one can stand in the garden of the Elands Bay Hotel  and with a bit of luck we may observe whales in the bay. </p>
We  then continue on  our journey through the Sandveld with its interesting round potato fields, all  the way through Leipoldtville, Lambert&rsquo;s Bay and Graaffwater in the direction of Clanwilliam &amp;  Klawer where we will be staying at the local<strong> </strong>Hotel for the following two nights.</td>
                </tr>
                <tr>
                  <td width="133" valign="top"><strong>Day 5: Thursday</strong></td>
                  <td width="543" valign="top">We  depart for Nieuwoudtville (in the Northern Cape) where we divert to Loeriesfontein direction to visit the largest concentration  of Quiver trees found anywhere on  earth. It stands amongst a yellow  flower carpet during a good flowering season.  It is quite a sight to behold. We then traverse the area and visit the  Nieuwoudtville waterfall. This region  is renowned for the most beautiful carpets of flowers and is the  flower-bulb capital of the world. We enjoy lunch at the Matjiesfontein-Boereplaas.
                    <p>This sandstone  building Dutch Reformed Church in town is a national  monument and has an interesting  history. After lunch we continue to  drive through the region, enjoying  the magnificence of its rich flowering splendor. Returning the way we had come,  we descend along the Van Rhyns Pass,  one of the most spectacular passes  in the country. From the top of this pass we are able to see the whole  Knersvlakte below &ndash; a truly stunning sight. Back down below in Vanrhynsdorp we  visit the biggest succulent nursery in the country, where we will be able to  view the succulent display and even buy some &ldquo;bababoutjies&rdquo; and &ldquo;krapogies&rdquo;.&nbsp; Time permitting, we&rsquo;ll pay a visit to a wine  cellar.</p>
Overnight in Clanwilliam/Klawer.</td>
