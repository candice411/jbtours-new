                  <td width="543" valign="top">After breakfast we depart for the Cederberg mountains  where we will visit the gravesite of C.Louis Leipoldt in the Pakhuis Mountain  Pass which is part of the Cederberg Range. The stunning rock formations is an absolute essential part of any  tour-group's itinerary and once  gazed upon one  can easily understand why C.L. Leipoldt chose this mountain kingdom for his  final resting place. 
                    <p>We then return to Cape Town via Citrusdal where we  follow the route between the Cederberg Range and the Olifants River, onwards via the N7 over the Piekenierskloof Pass. We  will continue through the Swartland  with its blankets of Canola Flowers bordering the road, past Malmesbury and a  brief visit to Darling to enjoy &lsquo;Evita se Perron&rsquo;, onto Cape Town where we end  our tour exactly where we had started 4 days earlier. </p>
Overnight at a hotel in Cape Town.</td>
