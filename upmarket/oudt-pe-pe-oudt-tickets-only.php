			  <p><strong>Oudtshoorn to Port Elizabeth (return) </strong><br />
The  Premier Classe train departs from Oudtshoorn to Port   Elizabeth every Saturday, returning from Port Elizabeth to Oudtshoorn every Sunday. The duration of the trip between Oudtshoorn and Port Elizabeth is  approximately 8.5 hours. Please note that the stop-overs do not allow  for excursions off the train.</p>
