              <p><strong>Saturday&rsquo;s  Schedule from Oudtshoorn to Port Elizabeth</strong><br />
                07:00&nbsp;  Guests convene at the Oudtshoorn Train station<br />
                07:30&nbsp; Depart from Oudtshoorn Train station<br />
                13:00&nbsp;  Lunch is served in the Dining Car<br />
                16:00&nbsp; The  train arrives in Port Elizabeth</p>
