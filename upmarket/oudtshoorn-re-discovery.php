			  <p>You have probably heard  about the historical &lsquo;Swartberg&rsquo; Mountain Pass outside Oudtshoorn, but have never traversed it. Did you  know about the existence of the spectacular &lsquo;Rust en Vrede&rsquo; waterfall close to the Cango Caves?  Do you know the story behind the beautiful Meiringspoort Road between De Rust and Beaufort West? Have you ever enjoyed  the artistic coffee shops in the town of Prince Albert? </p>
